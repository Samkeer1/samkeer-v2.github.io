////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
if(x > y){
  return y;
}
return x;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
if(x === 0){
  return true;
}else if(x === 1){
  return false;
}else if(x < 0){
  return isEven(x + 2);
}else if(x > 0){
  return isEven(x - 2);
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  let counter = 0;
for(let i = 0; i < string.length; i++){
  if(string[i] === letter){
    counter++;
  }
}
return counter;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let counter = 0;
for(let i = 0; i < string.length; i++){
  if(string[i] === "B"){
    counter++;
  }
}
return counter;
}


